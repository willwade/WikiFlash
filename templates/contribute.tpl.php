<p><a name="quicksteps"></a><b>Quick 6 step guide to editing..</b></p>
<p>Firstly; Don't Panic! Its Easy! 
<ol><li>Create an account. <a href="http://en.wikipedia.org/w/index.php?title=Special:UserLogin&type=signup" target="_new">Go here and follow the steps</a>. </li>
<li>You can now login. Do so <a href="http://en.wikipedia.org/w/index.php?title=Special:UserLogin&returnto=Special:UserLogin">here</a>, or look for the link at the top right of the wikipedia page (any page)</li>
<li>Are you creating a new page or editing an old one? Not sure if your page exists already? Search for it.. Go to wikipedia. Find the box on the far left hand side just below the logo which says "search". Enter something short and snappy. See if it comes up. If it does then click on that page and get on with editing if it needs it! (see step 4) If you can't see the correct page or the page returned says "No article title matches" then you need to make a new page. Firstly <a href="http://en.wikipedia.org/wiki/Wikipedia:Your_first_article">read this</a>: it should help to clear most things up. Click on the "create the page" link that comes up from the search results page and continue.. with editing (skip step 4!)</li>
<li>Go to your page you want to edit. Click on edit. Its located right at the top of the page. Alternativley look for a heading in the document you want to edit and click on the "edit" link. Note the latter is easier to work with but does mean you can't play around with any of the other sections</li>
<li>Use the buttons directly above the big text box to format your text. So highlight your text you want to make nice and different (e.g. Bold some text) and click the relevant button. If you hover your mouse above the button it will explain what it does.</li>
<li>Once you have made your changes, add a little comment in the comment box (so others can tell what you have done) and click on "show preview" to see that it looks how it should. If thats OK then click the "save page" button</li>
</ol>
Note that there are two main types of pages you can edit. (I'm ignoring things like images for now). Talk: pages and main pages. Talk pages aren't displayed normally unless you go looking for it. Each page has a Talk page and you can think of it an area to discuss ideas about how the page should evolve. To find a page's talk page click on the "discussion" link at the top of every wikipedia page (next to the "article link). If your concerned that you are going to press the big delete key and delete the entire contents of the internet then fear not - try writing a little comment in a Talk page. <br />And now for some very quick formatting tips:
<ul><li>Always link to other wikipedia pages if you can. Its like referencing just better!</li>
<li>Reference, reference, reference! Use the "ref" button to do this just like you would a normal written article and place your reference in the angle brackets</li>
<li>Comment your edit so we can tell what you have done!</li>
<li>Have fun!</li></ul>
